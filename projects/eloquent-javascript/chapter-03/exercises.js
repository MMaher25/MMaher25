// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  // Accepts 2 parameters, returns the smaller of the 2.
  if (x<y){
    return x;
  } else {
    return y;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven (num) {
  // Check to see if a number is 0 or 1
  // If 0, return true, if 1, return false
  // if neither, call same function on number - 2
  if (num >= 0){
    if (num === 0) {
      return true;
    } else if (num === 1) {
      return false;
    } else {
      return isEven(num-2);
    }
  }
  // Applys the same logic, but for negativfe numbers and adding 2 instead of subtracting
  else if (num < 0){
    if (num === 0) {
      return true;
    } else if (num === -1) {
      return false;
    } else {
      return isEven(num+2);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  // Returns how many times character char is found in string
  let counter = 0;
  for (let x = 0; x < string.length; x++) {
    if (string[x] === char) {
      counter += 1;
    }
  }
  return counter;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(stringB) {
  return countChars(stringB, "B");
}

